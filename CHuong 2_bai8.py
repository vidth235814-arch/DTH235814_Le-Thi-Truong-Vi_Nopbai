# Các loại lỗi khi lập trình và cách bắt lỗi trong Python
# - Khi viết chương trình có thể phát sinh lỗi (exception).
# - Một số lỗi thường gặp:
#   + ZeroDivisionError: chia cho 0.
#   + ValueError: nhập sai kiểu dữ liệu (ví dụ nhập chữ khi yêu cầu số).
# - Để xử lý lỗi, Python dùng cấu trúc try ... except.
# - Nếu không xử lý, chương trình sẽ bị dừng ngay khi lỗi xảy ra.